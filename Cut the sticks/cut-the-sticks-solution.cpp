vector<int> cutTheSticks(vector<int> arr) {
    int n = arr.size(),min,index;
    vector<int> answer;
    answer.push_back(n);
    sort(arr.begin(),arr.end());
    min=*min_element(arr.begin(), arr.end());
    while(n>1){
        index=-1;
        for(auto& element : arr){
            element -= min;
            if (element < min && element<=0){
                index++;
            }
        }
        arr.erase(arr.begin() , arr.begin()+index+1);
        min=*min_element(arr.begin(), arr.end());
        n = arr.size();
        if (n!=0)
            answer.push_back(n);
    }
    return answer;
}

